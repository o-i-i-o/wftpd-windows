## 🐛 **问题排查**

### **如果内存仍然很高（> 150 MB）**

**可能原因**:
1. 日志文件过大
2. 其他模块内存占用高
3. Windows 本身内存管理策略

**排查步骤**:
```bash
# 1. 检查日志文件大小
Get-ChildItem "C:\ProgramData\wftpg\logs" | Sort-Object Length -Descending

# 2. 清理旧日志
Remove-Item "C:\ProgramData\wftpg\logs\*.log.*" -Recurse

# 3. 使用性能分析工具
cargo install cargo-flamegraph
cargo flamegraph --bin wftpg
```

---

### **如果出现日志丢失**

**调整参数**:
```rust
// 在 log_tab.rs 和 file_log_tab.rs 中
const MAX_DISPLAY_LOGS: usize = 800;  // 从 500 上调到 800
const INITIAL_FETCH_COUNT: usize = 150;  // 从 100 上调到 150
```

