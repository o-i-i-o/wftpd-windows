# 模拟 FTP 连接产生日志
for i in range(100):
    try:
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        sock.connect(('localhost', 21))
        data = sock.recv(1024)
        sock.close()
    except:
        pass
    time.sleep(0.01)  # 10ms 间隔
```

**预期结果**:
- ✅ UI 保持流畅
- ✅ 无明显卡顿
